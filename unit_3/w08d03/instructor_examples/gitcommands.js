//GIT COMMANDS

// git init => start a new git repository
// git add . => add all files to staging
// git commit -m "Message" => commit files from staging with message
// git remote add <remoteName> <remoteUrl> => add remote under certain name
// git remote => list all existing remote repositories
// git remote rm <remoteName> => remove the remote of the particulare name
// git push <remoteName> <branch> => push to particular branch of remote
// git pull <remoteName> <branch> => pull and merge code from remote branch
// git branch => list branches of repository
// git checkout -b <branchName> => create a new branch
// git checkout <branchName => switch to particular branch
// git merge <branchName> => merge contents of named branch to current branch
// git clone <remoteURL> <folderName> => make a clone of repository in a new folder of given name
// git status => see which files are in/out of staging to be committed

